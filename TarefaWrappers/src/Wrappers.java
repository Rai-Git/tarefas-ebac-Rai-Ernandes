public class Wrappers
{
    public static void main(String args[])
    {
        int cpf = 125896;
        System.out.println(cpf);
        Integer cpf1 = cpf;
        System.out.println(cpf1);
        System.out.println();

        int caixamenor = 1256863322;
        Long caixamaior = (long) caixamenor;
        System.out.println(caixamenor);
        System.out.println(caixamaior);

    }
}
